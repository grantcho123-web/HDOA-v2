export default async function BlogPostPage({ params }) {
  const { slug } = await params;
  const post = POSTS[slug];
  if (!post) notFound();

  return (
    <div style={{ maxWidth:720, margin:'0 auto' }}>
      <a href="/blog" style={{ fontSize:12, color:'#34d399', textDecoration:'none', display:'inline-block', marginBottom:16 }}>← Back to Blog</a>
      <h1 style={{ fontSize:28, fontWeight:700, color:'white', lineHeight:1.3, marginBottom:8 }}>{post.title}</h1>
      <p style={{ fontSize:12, color:'#52525b', marginBottom:24 }}>{post.date} · {post.readTime} read</p>
      <div style={{ fontSize:14, color:'#a1a1aa', lineHeight:1.8 }}>
        {post.content.split('\n').map((line, i) => {
          const trimmed = line.trim();
          if (!trimmed) return <br key={i} />;
          if (trimmed.startsWith('## ')) return <h2 key={i} style={{ fontSize:20, fontWeight:700, color:'white', marginTop:28, marginBottom:8 }}>{trimmed.slice(3)}</h2>;
          if (trimmed.startsWith('- **')) {
            const m = trimmed.match(/^- \*\*(.+?)\*\*\s*[—-]\s*(.+)$/);
            if (m) return <div key={i} style={{ padding:'4px 0', fontSize:13 }}><span style={{ color:'#6ee7b7', fontWeight:600 }}>{m[1]}</span> <span style={{ color:'#71717a' }}>—</span> {m[2]}</div>;
            return <div key={i} style={{ padding:'4px 0', fontSize:13 }}>{trimmed.slice(2)}</div>;
          }
          if (trimmed.startsWith('- ')) return <div key={i} style={{ padding:'2px 0 2px 12px', fontSize:13, color:'#a1a1aa' }}>• {trimmed.slice(2)}</div>;
          if (trimmed.match(/^\d+\./)) return <div key={i} style={{ padding:'3px 0', fontSize:13, color:'#d4d4d8' }}>{trimmed}</div>;
          const linkified = trimmed.replace(/\[(.+?)\]\((.+?)\)/g, '<a href="$2" style="color:#34d399">$1</a>');
          return <p key={i} style={{ marginBottom:10, fontSize:14 }} dangerouslySetInnerHTML={{ __html: linkified }} />;
        })}
      </div>
      <div style={{ borderTop:'1px solid rgba(255,255,255,.05)', marginTop:32, paddingTop:16, textAlign:'center' }}>
        <p style={{ fontSize:13, color:'#71717a' }}>Ready to start? <a href="/" style={{ color:'#34d399' }}>Browse 500+ listings</a> or <a href="/earnings" style={{ color:'#34d399' }}>calculate your earnings</a>.</p>
      </div>
    </div>
  );
}
